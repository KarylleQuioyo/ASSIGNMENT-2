import numpy as np

# Examples of linalg.det method


# Example 1
M = np.array([[6, 2], [3, 7]])
M_det = np.linalg.det(M)
print("Determinant of M (Example 1):", M_det)


# Example 2
N = np.array([[1, 2, 3], [9, 8, 7], [4, 6, 7]])
N_det = np.linalg.det(N)
print("Determinant of N (Example 2):", N_det)


# Example 3
O = np.array([[3, 6], [9, 7]])
O_det = np.linalg.det(O)
print("Determinant of O (Example 3):", O_det)