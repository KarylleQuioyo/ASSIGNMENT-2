import numpy as np

# Examples of flatten method

# Example 1
P = np.array([[7, 4, 1], [3, 0, 4]])
P_flat = P.flatten()
print("Flattened P (Example 1):", P_flat)

# Example 2
Q = np.array([[5, 7, 0], [1, 6, 3], [4, 6, 2]])
Q_flat = Q.flatten()
print("Flattened Q (Example 2):", Q_flat)

# Example 3
R = np.array([[4, 5], [1, 0]])
R_flat = R.flatten()
print("Flattened R (Example 3):", R_flat)
