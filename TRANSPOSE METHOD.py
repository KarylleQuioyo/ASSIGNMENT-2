import numpy as np

# Examples of transpose method

# Example 1
G = np.array([[9, 8, 7], [6, 5, 4], [3, 2, 1]])
G_trans = G.transpose()
print(f"Transpose of G (Example 1):\n", G_trans)

# Example 2
H = np.array([[2, 3], [4, 5]])
H_trans = H.transpose()
print("Transpose of H (Example 2):\n", H_trans)

# Example 3
I = np.array([[5, 7, 4], [3, 9, 1]])
I_trans = I.transpose()
print("Transpose of I ( Example 3): \n", I_trans)